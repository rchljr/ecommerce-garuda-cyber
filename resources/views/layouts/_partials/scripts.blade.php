<script>
    window.addEventListener('scroll', function () {
        const header = document.querySelector('.header_section');
        if (window.scrollY > 50) {
            header.classList.add('header-scrolled');
        } else {
            header.classList.remove('header-scrolled');
        }
    });

</script>

